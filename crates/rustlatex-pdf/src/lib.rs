                // Handle VSkip-only lines: advance current_y and skip text rendering
                if line.nodes.len() == 1 {
                    if let BoxNode::VSkip { amount } = &line.nodes[0] {
                        current_y -= *amount as f32;
                        continue;
                    }
                }
                // Compute line natural width and glue info
